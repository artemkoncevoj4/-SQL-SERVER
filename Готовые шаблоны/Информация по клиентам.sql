use Интернет_провайдер;
SELECT
	d.[id договора],
	k.Имя,
	k.Фамилия,
	k.Отчество,
	p.[Название провайдера],
	tf.[Название тарифа],
	tf.[Абоненская плата],
	yp.[Дата начала],
	yp.[Дата окончания]
FROM Договоры d
INNER JOIN Клиенты k
ON d.[id клиента] = k.id
INNER JOIN [Услуги по договору] yp
ON yp.[id договора] = d.[id договора]
INNER JOIN [Тарифные планы] tf
ON yp.[id тарифа] = tf.[id тарифа]
INNER JOIN [Провайдер] p
ON d.[id провайдера] = p.id